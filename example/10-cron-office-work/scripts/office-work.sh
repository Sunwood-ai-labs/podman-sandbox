#!/bin/bash
# Claude Meeting Bot - 定時会議スクリプト
# 1時間おきに「会議」を開いてミッションを遂行

set -e

TIMESTAMP=$(date +"%Y%m%d_%H%M")
DATE=$(date "+%Y-%m-%d")
TIME=$(date "+%H:%M")
WORKSPACE="/workspace"
REPORT_FILE="${WORKSPACE}/meeting_${TIMESTAMP}.md"

# ミッション（このプロジェクトの改善ミッション）
MISSION="Podman学習リポジトリの改善とサンプルコードの品質向上"

log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1"
}

log "=== 🏢 定時会議開始 ==="
log "時刻: ${TIME}"

# 会議のアジェンダ（ローテーション）
AGENDAS=(
    "コードレビュー: 既存のサンプルコードの改善点を特定し、チームを編成して担当を割り当てる"
    "技術調査: Podmanの新機能やベストプラクティスを調査し、調査チームを編成する"
    "ドキュメント改善: READMEやコメントの充実案を出し、執筆チームを編成する"
    "テスト計画: サンプルスクリプトの動作確認方法を検討し、QAチームを編成する"
    "アイデア出し: 新しいサンプル例のアイデアをブレインストーミングし、実装チームを提案する"
    "リファクタリング: 重複コードや改善可能な箇所を見つけ、リファクタリングチームを編成する"
    "セキュリティ確認: コンテナ設定のセキュリティベストプラクティスを確認し、セキュリティチームを編成する"
    "パフォーマンス: ビルド時間やイメージサイズの最適化案を出し、最適化チームを編成する"
)

# アジェンダを選択（時間帯でローテーション）
HOUR=$(date +"%H")
AGENDA_INDEX=$((HOUR % ${#AGENDAS[@]}))
SELECTED_AGENDA="${AGENDAS[$AGENDA_INDEX]}"

log "📋 本日のアジェンダ: $SELECTED_AGENDA"

# Claude に議論を依頼
log "🤖 Claude に会議参加を依頼中..."

MEETING_OUTPUT=$(claude -p "あなたは定時会議のファシリテーター兼チームリーダーです。

## ミッション
${MISSION}

## 本日のアジェンダ
${SELECTED_AGENDA}

## 成果物の出力先
すべての成果物（コード、ドキュメント、設定ファイル等）は /workspace ディレクトリに出力してください。

## タスク
以下の内容を含む議事録を作成してください：

### 1. 現状分析
- 現在の状況と課題

### 2. エージェントチームの編成
以下のロールから必要なメンバーを選び、チームを編成してください：
- 🔍 **リサーチャー**: 調査・情報収集担当
- 💻 **デベロッパー**: 実装・コード変更担当
- 📝 **ライター**: ドキュメント作成担当
- 🧪 **テスター**: 動作確認・テスト担当
- 🎨 **デザイナー**: UI/UX改善担当

各メンバーの具体的なタスクを割り当ててください。

### 3. アクションアイテム
- 優先度付きのタスクリスト
- 担当者の割り当て

### 4. 懸念事項・リスク
- 潜在的な問題点

日本語で、チーム運営を意識した議事録形式で出力してください。" 2>&1) || MEETING_OUTPUT="⚠️ 会議実行エラー"

# マークダウンレポートを作成（会議ごとに新規ファイル）
mkdir -p "$WORKSPACE"

cat > "$REPORT_FILE" <<EOF
# 📅 ${DATE} ${TIME} 定時会議

**プロジェクト**: Podman Sandbox
**ミッション**: ${MISSION}
**アジェンダ**: ${SELECTED_AGENDA}

---

## 議事録

${MEETING_OUTPUT}

---

*Generated by Claude Meeting Bot*
EOF

log "✅ 会議終了"
log "📄 議事録: ${REPORT_FILE}"
log ""
log "=== 🏠 退勤します ==="
